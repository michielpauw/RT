#include "libft.h"

t_3v	ft_3v_scalar(t_3v v, double s)
{
	t_3v	to_return;
	int		i;

	i = 0;
	while (i < 3)
	{
		(to_return.v)[i] = (v.v)[i] * s;
		i++;
	}
	return (to_return);
}
